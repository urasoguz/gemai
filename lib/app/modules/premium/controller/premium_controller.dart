  // Trial ba≈ülatma fonksiyonu - Dinamik paket se√ßimi
  Future<void> startTrial() async {
    isPurchasing.value = true;
    try {
      // RevenueCat'ten offerings al
      final offerings = await Purchases.getOfferings();
      final offering = offerings.current;

      if (offering == null) {
        ShrineDialogService.showError(
          'premium_no_packages'.tr,
          AppThemeConfig.textHint,
          duration: const Duration(seconds: 3),
        );
        return;
      }

      // Se√ßili plan indeksini al
      final selectedIndex = selectedPlan.value;
      Package? package;

      // Paketleri kontrol et ve se√ßili paketi al
      if (packages.isNotEmpty && selectedIndex < packages.length) {
        // Kullanƒ±cƒ±nƒ±n se√ßtiƒüi paketi al
        package = packages[selectedIndex];
      } else if (packages.isNotEmpty) {
        // Eƒüer se√ßili indeks ge√ßersizse ilk paketi al
        package = packages.first;
      }

      if (package == null) {
        ShrineDialogService.showError(
          'premium_no_selected_package'.tr,
          AppThemeConfig.textHint,
          duration: const Duration(seconds: 3),
        );
        return;
      }

      // Se√ßili paketi satƒ±n al
      await Purchases.purchasePackage(package);

      // ‚úÖ Ba≈üarƒ±lƒ± satƒ±n alma sonrasƒ± - Ana sayfaya y√∂nlendir ve verileri yenile
      await _handleSuccessfulPurchase();
    } catch (e) {
      debugPrint('üî• Trial ba≈ülatma hatasƒ±: $e');

      if (e.toString().contains('cancelled')) {
        ShrineDialogService.showInfo(
          'premium_purchase_cancelled'.tr,
          AppThemeConfig.primary,
          duration: const Duration(seconds: 3),
        );
      } else {
        ShrineDialogService.showError(
          'premium_purchase_failed'.tr,
          AppThemeConfig.textHint,
          duration: const Duration(seconds: 3),
        );
      }
    } finally {
      isPurchasing.value = false;
    }
  }

  /// Ba≈üarƒ±lƒ± satƒ±n alma sonrasƒ± yapƒ±lacak i≈ülemler
  Future<void> _handleSuccessfulPurchase() async {
    try {
      if (kDebugMode) {
        print('üîÑ Satƒ±n alma ba≈üarƒ±lƒ± - Ana sayfaya y√∂nlendiriliyor...');
      }

      // Kullanƒ±cƒ±ya ba≈üarƒ± mesajƒ± g√∂ster
      ShrineDialogService.showSuccess(
        'premium_activated'.tr,
        AppThemeConfig.primary,
        duration: const Duration(seconds: 3),
      );

      // √ñnce kullanƒ±cƒ± verilerini g√ºncelle
      if (Get.isRegistered<UserController>()) {
        await userController.getUsers();
        if (kDebugMode) {
          print('‚úÖ Kullanƒ±cƒ± verileri satƒ±n alma sonrasƒ± g√ºncellendi');
        }
      }

      // Sonra ana sayfaya y√∂nlendir
      Get.offAllNamed(AppRoutes.home);
    } catch (e) {
      if (kDebugMode) {
        print('‚ùå Satƒ±n alma sonrasƒ± i≈ülemlerde hata: $e');
      }
    }
  }

  // Restore i≈ülemini ba≈ülatƒ±r (Merkezi servis ile)
  Future<void> restorePurchases() async {
    isRestoring.value = true;
    try {
      if (kDebugMode) {
        print('üîÑ Premium ekranƒ± restore i≈ülemi ba≈ülatƒ±lƒ±yor...');
      }

      // Merkezi restore servisi kullan
      final restoreService = Get.find<RestorePremiumService>();
      final success = await restoreService.performCompleteRestore();

      if (!success) {
        // Hata mesajƒ± servis tarafƒ±ndan g√∂sterildi
        if (kDebugMode) {
          print('‚ùå Premium ekranƒ± restore ba≈üarƒ±sƒ±z');
        }
      } else {
        if (kDebugMode) {
          print('‚úÖ Premium ekranƒ± restore ba≈üarƒ±lƒ±');
        }
      }
    } catch (e) {
      if (kDebugMode) {
        print('‚ùå Premium ekranƒ± restore exception: $e');
      }
      ShrineDialogService.showError(
        'premium_restore_error'.tr,
        AppThemeConfig.textHint,
        duration: const Duration(seconds: 3),
      );
    } finally {
      isRestoring.value = false;
    }
  }

